package 입출력;

import java.util.Scanner;

public class Main10869_사칙연산 {

	public static void main(String[] args) {
		int A,B;
		Scanner scan = new Scanner(System.in);
		
		A = scan.nextInt();
		B = scan.nextInt();
		System.out.println(A+B);
		System.out.println(A-B);
		System.out.println(A*B);
		System.out.println(A/B);
		System.out.println(A%B);
	}

}
